    private void btn_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IngresarActionPerformed
        
        
        int cont = 0;
        
        ArrayList usuarios=new ArrayList();
        ArrayList contrasenias=new ArrayList();
        
        
        if(rdt_Admin.isSelected() == true){
        
        controlador.Logueo(user, "1");
        
        usuarios = user.getUsuarios();
        contrasenias = user.getContrasenias();
        
        for (int i=0; i<usuarios.size(); i++){
        
            if(usuarios.get(i).toString().equals(txt_Usuario.getText()) && contrasenias.get(i).toString().equals(txt_Password.getText())){
            
            cont = 1;
            
            }
            
            if(cont == 1){
        
            Acceso_Admin a=new Acceso_Admin();
            this.setVisible(false);
            a.setVisible(true);
            
        }
        
        }
        }
        
        if (rdt_Encargado.isSelected() == true){
        
        controlador.Logueo(user, "2");
        
        usuarios = user.getUsuarios();
        contrasenias = user.getContrasenias();
        
        for (int i=0; i<usuarios.size(); i++){
        
            if(usuarios.get(i).toString().equals(txt_Usuario.getText()) && contrasenias.get(i).toString().equals(txt_Password.getText())){
            
            cont = 1;
            
            }
            
            if(cont == 1){
        
            Acceso_Encargado a=new Acceso_Encargado();
            this.setVisible(false);
            a.setVisible(true);
            
        }
        
        }
            
        }
        
        if(cont == 0){
        
            JOptionPane.showMessageDialog(null, "Credenciales Incorrectas", "ERROR", 0);
            
        }
        
        
        
    }//GEN-LAST:event_btn_IngresarActionPerformed

