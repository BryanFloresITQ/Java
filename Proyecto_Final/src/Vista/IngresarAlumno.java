    private void btn_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IngresarActionPerformed
if(txt_CI.getText() == "" || txt_Nombre1.getText() == "" || txt_Nombre2.getText() == "" || txt_Apellido1.getText() == "" || txt_Apellido2.getText()== ""){

    JOptionPane.showMessageDialog(null, "Datos Incompletos", "ERROR", 0);

}
else{
        Alumno ingresar=new Alumno( 0, "", "");
        Controlador controlador=new Controlador();
        
        ingresar.setCI(Integer.parseInt(txt_CI.getText()));
        ingresar.setNombres(txt_Nombre1.getText()+" "+txt_Nombre2.getText());
        ingresar.setApellidos(txt_Apellido1.getText()+" "+txt_Apellido2.getText());
        
        controlador.IngresarAlumno(ingresar);
        
        txt_Apellido1.setText("");
        txt_Apellido2.setText("");
        txt_CI.setText("");
        txt_Nombre1.setText("");
        txt_Nombre2.setText("");
        
        }
        cont = 0;
    }//GEN-LAST:event_btn_IngresarActionPerformed

